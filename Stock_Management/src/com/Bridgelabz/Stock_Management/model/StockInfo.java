package com.Bridgelabz.Stock_Management.model;

public class StockInfo {
	
	private String name;
	private int number_of_share;
	private int share_price;
	
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getNumber_of_share() {
		return number_of_share;
	}
	
	public void setNumber_of_share(int number_of_share) {
		this.number_of_share = number_of_share;
	}
	
	public int getShare_price() {
		return share_price;
	}
	
	public void setShare_price(int share_price) {
		this.share_price = share_price;
	}

}
